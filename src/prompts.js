export const REPLICATION_PROMPT = `请将我上传的图片复刻为高质量、可编辑的 PPTX 文件。不要把整张原图直接作为背景使用，而是进行元素级拆解、重绘和重排。

核心要求:
1. 一比一复刻原图版式、比例、颜色、文字（用微软雅黑字体）、图形、图标、箭头、表格、图表和层级关系。
2. 文本必须全部使用 PPT 原生文本框重建，内容准确，中文清晰，无乱码、无溢出、无遮挡。
3. 简单图形、色块、线条、箭头、流程框、圆角矩形、边框、背景区域等，必须优先使用 PPT 原生 Shape 对象绘制，去除多余阴影。
4. 表格使用 PPT 原生表格或可编辑形状组合重建。
5. 饼图、柱状图、折线图、环形图等图表，优先使用 PPT 原生图表对象重建；无法准确读取数据时按视觉比例估算并说明。
6. 所有图标必须使用生图模型重新绘制为透明背景图标，不能直接裁剪原图图标。
7. 所有复杂图片、复杂装饰、复杂形状、插画类元素，也必须使用生图模型重绘为透明背景素材，再按原图位置拼接进 PPT。
8. 所有重绘图标和复杂素材必须透明通道图层、无白边、无水印、无伪文字、无乱码，风格与原图一致。
9. 根据原图比例设置 PPT 页面尺寸，建立坐标映射，精确还原每个元素的位置、大小、角度和层级。
10. 完成后逐项检查并修复：文字错误、文字溢出、图形错位、图标变形、元素重叠、边缘裁切、透明背景异常、层级错误等问题。

工作流程:
第一步，分析原图结构和所有元素。
第二步，建立元素清单，区分文本、形状、箭头、表格、图表、图标、复杂图片。
第三步，用 PPT 原生对象重建文本、形状、箭头、表格和图表。
第四步，用生图模型逐个重绘所有图标和复杂图片为透明通道图层素材。
第五步，将所有对象按原图位置一比一拼接到 PPT。
第六步，对照原图进行复刻度检查，发现问题主动修复。
第七步，输出最终可编辑 PPTX 文件，并附简短复刻说明。

最终交付标准:
PPTX 链接；原图不作为整页背景；文本可编辑；简单形状可编辑；箭头可编辑；表格和图表尽量可编辑；图标和复杂图片为透明重绘素材；页面复刻度高；无乱码、无溢出、无错位、无裁切；整体视觉效果接近原图。`;

export function buildImagePrompt({ brief, templateSummary, pageCount, sampleMode }) {
  const modeText = sampleMode === "multi" ? "多页 PPT 样例图" : "单页 PPT 样例图";
  const profile = templateSummary?.trim()
    ? templateSummary.trim()
    : "未提供模板分析文本。请根据用户 brief 生成清晰、高级、咨询汇报风格的 PPT 样例。";

  return `你是 Ivy PPT Tool 的视觉方案设计师。请根据 PPT 模板风格和用户业务提示词，生成一段可直接用于生图模型的高质量 PPT 样例图 prompt。

生成目标：${modeText}
目标页数：${pageCount}

硬性要求：
- 生成的是 PPT slide mockup image，不是 PPTX 文件。
- 风格必须参考模板，不要偏离模板的颜色、字体、版式和商务感。
- 中文文字使用微软雅黑风格，清晰、可读、无乱码。
- 避免过小文字、伪文字、水印、杂乱图标。
- 明确画面结构、视觉层级、配色、标题、副标题、模块关系、图形和图标风格。
- 画面要适合后续被复刻成可编辑 PPTX。

用户提示词：
${brief.trim()}

模板风格/布局信息：
${profile}

请输出最终生图 prompt。`;
}
